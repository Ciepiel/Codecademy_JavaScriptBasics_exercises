//variable with desired amount of sugar
let cupsOfSugarNeeded = 3;
//variable to collect sugar added one by one
let cupsAdded = 0;

//do...while to add cup of sugar one to by one
//until cups added will reach sugar needded amount
do {
  cupsAdded++;
  console.log(cupsAdded)
} while (cupsAdded < cupsOfSugarNeeded);
